DROP TABLE FCT_SALES_DD CASCADE CONSTRAINTS;
CREATE TABLE FCT_SALES_DD(
    DIM_SELLERS_ID	    NUMBER      NOT NULL,
    DIM_STORES_ID	    NUMBER(8)   NOT NULL,
    DIM_CARS_ID	        NUMBER(8)   NOT NULL,
    EVENT_DT	        DATE        NOT NULL,
    START_DT            DATE        NOT NULL,
    END_DT              DATE        NOT NULL,
    DIM_CUSTOMERS_ID    NUMBER(8)   NOT NULL,
    ORDER_CODE          NUMBER(8)   NOT NULL,
    COST                NUMBER(8)   NOT NULL,
    MIN_PRICE           NUMBER(8)   NOT NULL,
    AVG_PRICE           NUMBER(8)   NOT NULL,
    SD_PRICE            NUMBER(8)   NOT NULL,
    INSERT_DT           DATE        NOT NULL,
    UPDATE_DT           DATE        NOT NULL,
    CONSTRAINT FACT_SELLER_ID FOREIGN KEY (DIM_SELLERS_ID) REFERENCES DIM_SELLERS (SELLER_ID),
    CONSTRAINT FACT_STORE_ID FOREIGN KEY (DIM_STORES_ID) REFERENCES DIM_STORES (STORE_ID),
    CONSTRAINT FACT_CAR_ID FOREIGN KEY (DIM_CARS_ID) REFERENCES DIM_CARS (CAR_ID),
    CONSTRAINT FACT_ORDER_DATE_ID FOREIGN KEY (EVENT_DT) REFERENCES DIM_DATE (DATE_ID),
    CONSTRAINT FACT_CUSTOMER_ID FOREIGN KEY (DIM_CUSTOMERS_ID) REFERENCES DIM_CUSTOMERS (CUSTOMER_ID));
