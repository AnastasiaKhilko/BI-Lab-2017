        shop_name         VARCHAR2 ( 100 CHAR ) ,
        phone             VARCHAR2 ( 50 CHAR )  ,
        address           VARCHAR2 ( 200 CHAR ) ,
        city_id           NUMBER   ( 10 )       ,
        manager_id        NUMBER   ( 10 )            
        );
        
        
 