CREATE TABLE CE_ORDERS
  (
    ORDERS_ID     NUMBER(8),
    ORDER_CODE    NUMBER(8),
    ORDER_DATE_ID DATE,
    COST          NUMBER(8),
    MIN_PRICE     NUMBER(8),
    AVG_PRICE     NUMBER(8),
    SD_PRICE      NUMBER(8),
    CARS_ID        NUMBER(8),
    SELLERS_ID     NUMBER(8),
    CUSTOMERS_ID   NUMBER(8),
    STORES_ID      NUMBER(8),
    CONSTRAINT ORDERS_ID PRIMARY KEY (ORDERS_ID),
    CONSTRAINT ORDER_DATES_ID FOREIGN KEY (ORDER_DATES_ID) REFERENCES CE_DATES (DATES_ID),
    CONSTRAINT ORDER_SELLERS_ID FOREIGN KEY (SELLERS_ID) REFERENCES CE_SELLERS (SELLERS_ID),
    CONSTRAINT ORDER_CUSTOMERS_ID FOREIGN KEY (CUSTOMERS_ID) REFERENCES CE_CUSTOMERS (CUSTOMERS_ID),
    CONSTRAINT ORDER_CARS_ID FOREIGN KEY (CARS_ID) REFERENCES CE_CARS (CARS_ID),
    CONSTRAINT ORDER_STORES_ID FOREIGN KEY (STORES_ID) REFERENCES CE_STORES (STORES_ID)
  );
