CREATE TABLE CE_CARS
  (
    CARS_ID                  NUMBER(8),
    CAR_CODE                 NUMBER(8),
    CAR_NAME                 VARCHAR2(200),
    CARS_REGISTRATION_DATE_ID DATE,
    VEHICLE_TYPES_ID          NUMBER(8),
    ENGINE_TYPES_ID           NUMBER(8),
    GEARBOX_TYPES_ID          NUMBER(8),
    MODELS_ID                 NUMBER(8),
    REPAIR_STATUS_ID         NUMBER(8),
    CONSTRAINT CARS_ID PRIMARY KEY (CARS_ID),
    CONSTRAINT CARS_REGISTRATION_ID FOREIGN KEY (CARS_REGISTRATION_DATES_ID) REFERENCES CE_DATES (DATES_ID),
    CONSTRAINT CAR_VEHICLE_TYPES_ID FOREIGN KEY (VEHICLE_TYPES_ID) REFERENCES CE_VEHICLE_TYPES (VEHICLE_TYPES_ID),
    CONSTRAINT CAR_ENGINE_TYPES_ID FOREIGN KEY (ENGINE_TYPES_ID) REFERENCES CE_ENGINE_TYPES (ENGINE_TYPES_ID),
    CONSTRAINT CAR_GEARBOX_TYPES_ID FOREIGN KEY (GEARBOX_TYPES_ID) REFERENCES CE_GEARBOX_TYPES (GEARBOX_TYPES_ID),
    CONSTRAINT CAR_MODELS_ID FOREIGN KEY (MODELS_ID) REFERENCES CE_MODELS (MODELS_ID),
    CONSTRAINT CAR_REPAIR_STATUS_ID FOREIGN KEY (REPAIR_STATUS_ID) REFERENCES CE_REPAIRED_STATUS (REPAIR_STATUS_ID)
  );
