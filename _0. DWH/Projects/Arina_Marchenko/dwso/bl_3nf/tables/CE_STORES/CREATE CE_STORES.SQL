CREATE TABLE CE_STORES
  (
    STORE_ID   NUMBER(8),
    STORE_CODE VARCHAR2(200),
    ADDRESSES_ID NUMBER(8),
    PHONE      VARCHAR2(200),
    EMAIL      VARCHAR2(200),
    CONSTRAINT STORES_ID PRIMARY KEY (STORES_ID),
    CONSTRAINT STORE_ADDRESSES FOREIGN KEY (ADDRESSES_ID) REFERENCES CE_ADDRESSES (ADDRESSES_ID)
  );
