CREATE TABLE CE_CUSTOMERS
  (
    CUSTOMERS_ID      NUMBER(8),
    CUSTOMER_CODE    VARCHAR2(200),
    CUSTOMER_SURNAME VARCHAR2(200),
    AGE              NUMBER(8),
    GENDER           NUMBER(8),
    PHONE            VARCHAR2(200),
    EMAIL            VARCHAR2(200),
    CONSTRAINT CUSTOMERS_ID PRIMARY KEY (CUSTOMERS_ID)
  );
