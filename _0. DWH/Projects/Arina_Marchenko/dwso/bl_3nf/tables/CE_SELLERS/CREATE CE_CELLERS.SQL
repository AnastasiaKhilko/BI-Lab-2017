CREATE TABLE CE_SELLERS
  (
    SELLERS_ID      NUMBER(8),
    SELLERS_CODE    VARCHAR2(200),
    SELLER_SURNAME VARCHAR2(200),
    PHONE          VARCHAR2(200),
    EMAIL          VARCHAR2(200),
    CITIES_ID        NUMBER(8),
    CONSTRAINT SELLERS_ID PRIMARY KEY (SELLERS_ID),
    CONSTRAINT SELLER_CITY FOREIGN KEY (CITIES_ID) REFERENCES CITY (CITIES_ID)
  );
